package lesson3;

public class Task7_Display_sequence {
    public static void main(String[] args) {
// Create a loop. I should be less or equal to 14 because we have 14 numbers. //
// Multiplication the i with 7 to obtain sequence

        for (int i = 1; i <= 14; i++) {

            int num = i * 7;
            System.out.println(num);
        }

    }
}
