package lesson3;

public class Task5_Multiplication_Table {
    public static void main(String[] args) {
        // Create a loop to run the collection and print  the result by multiplication i by 3
        int i = 1;
        for (i = 1; i <= 10; i++) {
            int result = i * 3;
            System.out.println("3 * " + i + " = " + result);
        }
    }

}
