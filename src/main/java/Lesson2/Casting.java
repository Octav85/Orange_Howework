package Lesson2;

public class Casting {
    public static void main(String[] args) {
        int z = 10;
        System.out.println(z += 5);

//casting 1
        String nr = "5";
        Integer.parseInt(nr);
        System.out.println(nr + 5);
//casting 2
        double nr2 = 2.00;
        int x = (int) nr2;
        System.out.println(x);


    }

}





